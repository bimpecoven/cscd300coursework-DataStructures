public class Pair
{
   public int left;
   public int right;
   
   public Pair(final int left, final int right)
   {
      this.left = left;
      this.right = right;
   }//end EVC
   
   public String toString()
   {
      return "[" + left + ", " + right + "]";
   }//end toString
}